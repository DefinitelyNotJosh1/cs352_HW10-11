writeBoard(Board) :- write("   1 2 3 4 5 6 7 8 9 A B C D E F "), nl,
                     writeBoarder(f),
                     writeLines([1,2,3,4,5,6,7,8,9,a,b,c,d,e,f], Board).

writeLines([],[]).
writeLines([N|NS], [L|LS]) :- upcase_atom(N,A), write(A), write(" |"),
                              writeCells(L),
                              writeBoarder(N),
                              writeLines(NS,LS).

writeBoarder(N) :- N \= f, write("  |-+-+-+-+-+-+-+-+-+-+-+-+-+-+-|"), nl.
writeBoarder(N) :- N  = f, write("  +-----------------------------+"), nl.

writeCells([])     :- nl.
writeCells([X|NS]) :- upcase_atom(X,U), write(U), write('|'), writeCells(NS).

